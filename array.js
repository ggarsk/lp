let nomes = ['jorge', 'marcos', 'nando']

console.log(nomes[0])
console.log(nomes[1])
console.log(nomes[2])

/* Os tão temidos arrays nada mais são que variáveis com alta capacidade de armazenamento. Na let nomes, por exemplo, armazenei 3 nomes diferentes em uma única variável, coisa que, usando variáveis comuns, não seria possível. 

Os arrays são vetores que sempre começam em 0. Ou seja, suas posições vão de 0 até o tamanho total do vetor. 
A let nomes tem 3 nomes, mas começa no 0. Portanto, ao declarar no console.log que quero imprimir o valor da
posição 0 do vetor nomes, eu puxo o nome jorge, enquanto que, ao declarar no console.log que quero imprimir 
o valor da posição 2 do vetor nomes, imprimo o nome nando */

/* Arrays podem armazenar os tipos de variáveis string (tipo texto, como a let nomes), number (tipo número), boolean (true or false) e null (nulo ou vazio) */